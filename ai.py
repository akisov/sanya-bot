_client = None
_provider = None
_history: dict[int, deque] = defaultdict(lambda: deque(maxlen=12))


def init(groq_api_key: str = None, mistral_api_key: str = None) -> None:
    global _client, _provider
    if mistral_api_key:
        from mistralai import Mistral
        _client = Mistral(api_key=mistral_api_key)
        _provider = "mistral"
        print("AI: Mistral")
    elif groq_api_key:
        from groq import Groq
        _client = Groq(api_key=groq_api_key)
        _provider = "groq"
        print("AI: Groq")


def get_response(chat_id: int, user_text: str, username: str = "") -> str | None:
    if _client is None:
        return None

    content = f"{username}: {user_text}" if username else user_text
    _history[chat_id].append({"role": "user", "content": content})

    messages = [{"role": "system", "content": SYSTEM_PROMPT}] + list(_history[chat_id])

    try:
        if _provider == "mistral":
            resp = _client.chat.complete(
                model="mistral-small-latest",
                messages=messages,
                max_tokens=300,
            )
        else:
            resp = _client.chat.completions.create(
                model="llama-3.3-70b-versatile",
                messages=messages,
                max_tokens=300,
                temperature=1.0,
            )
        reply = resp.choices[0].message.content.strip()
        _history[chat_id].append({"role": "assistant", "content": reply})
        return reply
    except Exception as e:
        logger.error(f"AI error ({_provider}): {e}")
        return None
